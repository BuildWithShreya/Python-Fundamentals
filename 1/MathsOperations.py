import math

x = 3
y = 2

# ceil(x)
r_ceiling = math.ceil(x)
print("ceil(", x, ") =", r_ceiling)

# copysign(x, y)
r_copysign = math.copysign(x, y)
print("copysign(", x, ",", y, ") =", r_copysign)

# cos(x)
r_cos = math.cos(x)
print("cos(", x, ") =", r_cos)

# exp(x)
r_exp = math.exp(x)
print("exp(", x, ") =", r_exp)

# factorial(x)
r_factorial = math.factorial(x)
print("factorial(", x, ") =", r_factorial)

# floor(x)
r_floor = math.floor(x)
print("floor(", x, ") =", r_floor)

# fmod(x, y)
r_fmod = math.fmod(x, y)
print("fmod(", x, ",", y, ") =", r_fmod)

# gcd(x, y)
r_gcd = math.gcd(x, y)
print("gcd(", x, ",", y, ") =", r_gcd)

# log(x[, base])
r_log = math.log(x)
print("log(", x, ") =", r_log)

# log2(x)
r_log2 = math.log2(x)
print("log2(", x, ") =", r_log2)

# log10(x)
r_log10 = math.log10(x)
print("log10(", x, ") =", r_log10)

# pow(x, y)
r_pow = math.pow(x, y)
print("pow(", x, ",", y, ") =", r_pow)

# remainder(x, y)
r_remainder = math.remainder(x, y)
print("remainder(", x, ",", y, ") =", r_remainder)

# radians(x)
r_radians = math.radians(x)
print("radians(", x, ") =", r_radians)

# sqrt(x)
r_sqrt = math.sqrt(x)
print("sqrt(", x, ") =", r_sqrt)

# sin(x)
r_sin = math.sin(x)
print("sin(", x, ") =", r_sin)

# tan(x)
r_tan = math.tan(x)
print("tan(", x, ") =", r_tan)
