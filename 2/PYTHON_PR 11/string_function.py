# String functions
text= "government"

s1= text.capitalize()           # capitalize()
print(s1)

s2= text.count('e')             # count()
print(s2)

s3= text.find('n')              # find()
print(s3)

s4= text.endswith('t')          # endswith()
print(s4)

s5= text.index('v')             # index()
print(s5)

