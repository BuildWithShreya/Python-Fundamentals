print(s6)

s7= text.isalpha()              # isalpha()
print(s7)

s8= text.isdigit()              # isdigit()
print(s8)

s9= text.islower()              # islower()
print(s9)

s10= text.isupper()             # isupper()
print(s10)

str1= text.isnumeric()          # isnumeric()
print(str1)

str2= text.isspace()            # isspace()
print(str2)

